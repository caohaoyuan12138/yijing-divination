export default function ClassicReadingPanel() {
  const [selectedBook, setSelectedBook] = useState<ClassicBook | null>(null);
  const [selectedChapter, setSelectedChapter] = useState(0);

  if (!selectedBook) {
    return (
      <div className="panel">
        <h3 className="text-section-title flex items-center gap-2 mb-4">
          <span>📖</span> 典籍阅读
        </h3>
        <div className="space-y-2">
          {CLASSIC_BOOKS.map((book) => (
            <div
              key={book.id}
              onClick={() => { setSelectedBook(book); setSelectedChapter(0); }}
              className="p-3 bg-surface-alt rounded-lg hover:bg-surface-alt/80 cursor-pointer transition-colors"
            >
              <div className="font-bold text-gold text-sm">{book.name}</div>
              <div className="text-xs text-text-muted">{book.description}</div>
              <div className="text-xs text-text-secondary mt-1">共 {book.chapters.length} 章</div>
            </div>
          ))}
        </div>
      </div>
    );
  }

  const chapter = selectedBook.chapters[selectedChapter];

  return (
    <div className="panel">
      <div className="flex items-center justify-between mb-4">
        <button
          onClick={() => setSelectedBook(null)}
          className="text-xs text-text-muted hover:text-text-secondary"
        >
          ← 返回
        </button>
        <h3 className="text-section-title text-center">
          📖 {selectedBook.name}
        </h3>
        <div className="w-8" />
      </div>

      {/* 章节选择 */}
      <div className="flex gap-2 mb-3 overflow-x-auto pb-2">
        {selectedBook.chapters.map((ch, i) => (
          <button
            key={i}
            onClick={() => setSelectedChapter(i)}
            className={`px-2 py-1 rounded text-xs whitespace-nowrap transition-all ${
              i === selectedChapter
                ? 'bg-gold/10 text-gold border border-gold/30'
                : 'bg-surface-alt text-text-muted hover:bg-surface-alt/80 border border-border-light'
            }`}
          >
            {ch.title.substring(0, 6)}
          </button>
        ))}
      </div>

      {/* 章节内容 */}
      <div className="p-4 bg-surface-alt rounded-lg max-h-[400px] overflow-y-auto">
        <h4 className="font-bold text-gold mb-3">{chapter.title}</h4>
        <pre className="text-xs text-text-secondary whitespace-pre-wrap leading-relaxed font-kaishu">
          {chapter.content}
        </pre>
      </div>

      {/* 章节导航 */}
      <div className="flex justify-between mt-3">
        <button
          onClick={() => setSelectedChapter(Math.max(0, selectedChapter - 1))}
          disabled={selectedChapter === 0}
          className="px-3 py-1.5 bg-surface-alt rounded text-xs text-text-muted hover:bg-surface-alt/80 disabled:opacity-50"
        >
          ← 上一章
        </button>
        <button
          onClick={() => setSelectedChapter(Math.min(selectedBook.chapters.length - 1, selectedChapter + 1))}
          disabled={selectedChapter === selectedBook.chapters.length - 1}
          className="px-3 py-1.5 bg-surface-alt rounded text-xs text-text-muted hover:bg-surface-alt/80 disabled:opacity-50"
        >
          下一章 →
        </button>
      </div>
    </div>
  );
}
